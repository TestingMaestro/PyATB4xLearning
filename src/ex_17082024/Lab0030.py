# Arithemetic ops - //, % , **

# ----> positive--> normal division, omits decimal part---> floor division----> 3.5 ---> 3 [round off 3.5]
print(7 // 2)  # quotient

# ----> Negative --> Divides and provides o/p which is nearest to the number -3.5 => 4 [less than or equal to -3.5]
print(-7 // 2)  # quotient

print(10 % 3)  # remainder

print(-10 % 3)  # remainder

print(2 ** 3)  # exponential op [power]

